// Primitive Data Types Examples :

let number = 10;
let string = "Hello World!";
let boolean = true;
let nullValue = null;
let undefinedValue;
let symbol = Symbol("unique");
let bigInt = 9007199254740991n;

console.log(number);
console.log(string);
console.log(boolean);
console.log(nullValue);
console.log(undefinedValue);
console.log(symbol);
console.log(bigInt);

// Non-Primitive Data Types Examples :

let array = [1, 2, 3, 4, 5];
let object = { name: "John", age: 30 };
let func = function () {
  console.log("Hello World!");
};

console.log(array);
console.log(object);
console.log(func);
